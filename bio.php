<div class="cushycms"><h3>ARTIST&#39;S STATEMENT</h3>

<p class="indent">Creating is an addiction. With all the highs and lows you would expect.</p>

<p class="indent">I am interested in the surface textures and creating paintings that portray them in realistic detail. Hopefully a viewer will feel they can run a hand over the feathers and moss, or grasp an object in the painting.</p>

<p class="indent">This detail is usually composed in an abstract design. The design can occur naturally or be arranged by me.</p>

<p class="indent">The subjects of my paintings are usually based on the patterns and textures of nature and human interaction with nature. I have found subjects to paint in remote wilderness areas of the world, local parking lots, and my own backyard.</p>

